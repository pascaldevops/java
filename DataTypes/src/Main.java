public class Main {
    public static void main(String[] args) {
        // string
        String lastName = "Laurent";
        System.out.println("My name is: " + lastName);

        // Integers
        int myAge = 31;
        System.out.println("My int is: " + myAge);

        // Byte - small type
        byte mByte = 127;
        System.out.println("My byte is: " + mByte);

        // Short 2x bytes
        short mShort = 128;
        System.out.println("My short is: " + mShort);

        // Float 1.56
        float mFloat = 1.56f;
        System.out.println("My float is: " + mFloat);

        // Double
        double mDouble = 34.89;
        System.out.println("My double is: " + mDouble);

        // character
        char mChar = 'P';
        System.out.println("My char is: " + mChar);

        // boolean
        boolean mBool = true;
        System.out.println("My boolean is: " + mChar);

    }
}
